# レッスン８「リスト」

'''
【リストとは？】

「変数」は一つしか値を入れることができない。
「リスト」は複数の値をまとめて入れることができる。

・例えると「番号付きの棚」
・棚の中に何個でも値を並べて入れられる
・棚のどの位置にあるか「番号（インデックス）」で取り出せる
'''


# ─────────────────────────────
# リストの基本
# ─────────────────────────────

'''
【リストのルール】
・書き方：リスト名 = ["要素0", "要素1", "要素2" ...]
・リスト名のルールは変数名のルールと同じ
・要素は ",(カンマ)" で区切る
・カッコは [ ]（角括弧）を使う
・要素には「インデックス（番号）」が振られ、"0" から始まる
'''

party = ["勇者", "魔法使い", "僧侶", "盗賊"]

print(party)        # リスト全体を表示
print(party[0])     # インデックス0 → "勇者"
print(party[2])     # インデックス2 → "僧侶"

# len()でリストの要素数（個数）を取得できる
print(len(party))   # 4




# ─────────────────────────────
# リストの操作
# ─────────────────────────────

# ① 末尾に要素を追加：append()
party.append("騎士")       # "盗賊" の後に "騎士" が追加される
print(party)

# ② 好きな位置に追加：insert(インデックス, 要素)
party.insert(1, "賢者")    # インデックス1の位置に "賢者" を挿入
print(party)               # 元のインデックス1以降の要素は番号がずれる

# ③ 要素名を指定して削除：remove()
party.remove("盗賊")       # "盗賊" をリストから削除（削除後、インデックスは振り直し）
print(party)

# ④ インデックスを指定して削除：pop()
party.pop(0)               # インデックス0（"勇者"）を削除
print(party)




# ─────────────────────────────
# for文とリストの組み合わせ  ← ここが重要！
# ─────────────────────────────

'''
for文はrange()だけでなく、リストと組み合わせることもできる。
「for 変数 in リスト名:」と書くと、
リストの要素を先頭から順番に一つずつ取り出してループする。
'''

monsters = ["スライム", "ゴブリン", "オーク", "ドラゴン"]

for monster in monsters:            # monstersの要素を順番にmonsterへ代入
    print("敵が現れた：", monster)  # 要素の数だけこの処理が繰り返される


# for文でリストの合計を計算する
scores = [85, 92, 67, 100, 78]

total = 0               # 合計を入れる変数を 0 で用意しておく
for score in scores:    # scoresの要素を順番にscoreへ代入
    total += score      # ループのたびに合計に足していく

print("合計点：", total)
print("平均点：", total / len(scores))  # 合計 ÷ 個数で平均を計算