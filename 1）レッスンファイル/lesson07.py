# レッスン７「くり返し（ループ）」


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ１】 while文
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

'''
while文は「条件式が True（正しい）の間、処理を繰り返す」構文。

while 条件式:
    処理        ← インデント（字下げ）を忘れずに！
'''

num = 1
while num < 5:      # "num < 5" が条件式。":" を忘れずに！
    print(num)      # 条件式が正しい間は、この処理が実行される
    num += 1        # "+=" は加算代入演算子。変数に値を足して再代入する

# 実行結果：1, 2, 3, 4

# 【注意】num += 1 を書き忘れると条件が変わらず、永遠に終わらない！
# → これを「無限ループ」という。止めるにはターミナル上で、Ctrl + C を押す。




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ２】 for文と range()
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

'''
for文は「指定した範囲の回数だけ処理を繰り返す」構文。
"i" はループ変数（i 以外の名前でもOK）。

for i in range(...):
    処理

range() の引数は3パターンある。
'''

# パターン① range(stop)
# → 0 から stop-1 まで
for i in range(5):
    print(i)    # 0, 1, 2, 3, 4


# パターン② range(start, stop)
# → start から stop-1 まで
for i in range(1, 6):
    print(i)    # 1, 2, 3, 4, 5


# パターン③ range(start, stop, step)
# → start から stop-1 まで、step（刻み）ごとに進む
for i in range(0, 10, 2):
    print(i)    # 0, 2, 4, 6, 8

# step にマイナスを指定すると、カウントダウンもできる
for i in range(5, 0, -1):
    print(i)    # 5, 4, 3, 2, 1




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ３】 break（ループの中断）
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

'''
break は、ループを途中で強制的に抜け出す命令。
「特定の条件になったらループを終わらせたい」ときに使う。
'''

# for文での break
for i in range(1, 6):
    if i == 3:
        break           # i が 3 になったらループを終了
    print(i)            # 1, 2 が表示される（3以降は表示されない）


# while True（意図的な無限ループ）＋ break パターン
# 「正しい入力が来るまで何度でも繰り返す」ときに特に便利！

while True:                             # 条件が常に True なので、break がなければ永遠に続く
    answer = input("パスワードを入力：")
    if answer == "python":
        print("ログイン成功！")
        break                           # 正解したらループを抜ける
    print("違います。もう一度入力してください。")