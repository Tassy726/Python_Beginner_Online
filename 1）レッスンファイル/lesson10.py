# レッスン１０「tkinter」

'''
「tkinter」とは、Pythonでデスクトップアプリ（ウィンドウやボタンなどのGUI）を
作るための標準ライブラリ。

・追加インストール不要（Pythonに最初から付属）
・ウィンドウ、ボタン、ラベル、入力欄などを簡単に作れる
・小規模なツールや学習用アプリ作成に向いている
'''


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ１】最小構成
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

import tkinter as tk    # tkinterモジュールを読み込む（"tk"という略称にする）

root = tk.Tk()          # メインウィンドウ（土台）を作成し、変数 root に代入

# ── ここにウィジェット（部品）を追加していく ──

root.mainloop()         # ウィンドウを表示し、操作を待ち続ける。必ず最後の行に書く！


# ウィンドウのプロパティ（属性）

root.title("My App")            # タイトルバーに表示する文字

root.geometry("600x300")        # ウィンドウのサイズ。"横x縦"のピクセル数で指定

root.configure(bg="skyblue")    # ウィンドウの背景色




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ２】ウィジェット（部品）の種類
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

'''
「ウィジェット」とは、ウィンドウに配置できる部品のこと。
・作っただけでは画面に表示されない！
・配置の命令（pack / place）を別途書く必要がある
'''

# ラベル（文字を表示する部品）
lb = tk.Label(root, text="Hello!", font=("Arial", 18))

# ボタン
btn = tk.Button(root, text="クリック！", font=("Arial", 12))

# 1行テキストボックス（参考）
# ent = tk.Entry(root, width=30)

# 複数行テキストボックス（参考）
# txt = tk.Text(root, width=40, height=5)




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ３】配置方法① pack()
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

# pack()：ウィジェットを上から順番に自動で並べる
lb.pack()
btn.pack()

# オプションで余白や並び方を調整できる
lb.pack(pady=20)        # 上下に20ピクセルの余白
btn.pack(padx=10)       # 左右に10ピクセルの余白
lb.pack(side="left")    # 横並びにする（"left" / "right" / "top" / "bottom"）




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【ステップ４】配置方法② place()
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━

'''
place()：座標を指定してウィジェットを配置する
・ウィンドウの左上が起点（x=0, y=0）
・x は右方向、y は下方向に数値が大きくなる
・自由に配置できる分、座標は全部自分で決める必要がある
'''

lb.place(x=50, y=80)    # x=50、y=80 の位置に配置
btn.place(x=200, y=150) # x=200、y=150 の位置に配置


# 【pack() と place() の使い分け】
#
#               pack()              place()
# 配置方法      自動で順番に並ぶ    座標を自分で指定
# 向いている場面 シンプルなレイアウト 凝ったレイアウト
# 手間          少ない              多い