# レッスン５「IF文② 比較演算子」


# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【比較演算子とは】
# 条件式の中で「２つの値を比べる」ための記号
#
# ==    ちょうど等しい          a == 10    "equal to"
# !=    等しくない              a != 10    "not equal to"
# >     より大きい（超える）     a > 10     "greater than"
# <     より小さい（未満）       a < 10     "less than"
# >=    以上（その数も含む）     a >= 10    "greater than or equal to"
# <=    以下（その数も含む）     a <= 10    "less than or equal to"
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━




# ① ">" と ">=" の違い

hp = 10

if hp > 10:                   # ">"は「10を超える」なので、10は含まない
    print("バリバリ元気！")
else:
    print("ちょっと疲れてる？")

# → hp = 10 のとき「ちょっと疲れてる？」が表示される（10は"10を超える"に当てはまらないから）


if hp >= 10:                  # ">="は「10以上」なので、10も含む
    print("バリバリ元気！")
else:
    print("ちょっと疲れてる？")

# → hp = 10 のとき「バリバリ元気！」が表示される（10は"10以上"に当てはまるから）




# ② "==" と "!=" の違い

hp = 10

if hp == 10:        # "=="は「ちょうど10と等しい」
    print("ちょうど10！")
else:
    print("10じゃない")

# hp を 9 や 11 に変えてみよう → 「10じゃない」に変わる


hp = 10

if hp != 10:        # "!="は"=="の正反対。「10と等しくない」
    print("10じゃない")
else:
    print("ちょうど10！")

# "==" と "!=" は結果がすべて逆になる




# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━
# 【elifの落とし穴：条件の順番に注意！】
# elif は「上から順番にチェックして、最初に当てはまった時点で終了」
# → 条件の順番を間違えると、意図しない結果になる！
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━


# ❌ 悪い例：広い条件を先に書いてしまったバージョン

hp = 75

if hp >= 10:        # "10以上"は広すぎる！75も30も全部ここで当てはまってしまう
    print("ピンチ！")
elif hp >= 50:
    print("元気！")  # ここは絶対に実行されない
else:
    print("ゲームオーバー！")

# → hp = 75 なのに「ピンチ！」と表示されてしまう（バグ！）


# ✅ 正しい例：狭い（厳しい）条件を先に書くバージョン

hp = 75

#   0 -------- 10 --------- 50 -------- 100
#       ↑              ↑            ↑
#   ゲームオーバー    ピンチ         元気
#   → 「狭い条件」を上に、「広い条件」を下に！

if hp >= 50:        # まず一番厳しい条件から
    print("元気！")
elif hp >= 10:      # 次に狭い条件
    print("ピンチ！")
else:               # どれにも当てはまらない場合
    print("ゲームオーバー！")

# hp を 75 / 30 / 5 に変えて、3パターン全部確認してみよう！


