# レッスン９「関数」

'''
「関数」とは、一連の処理に名前を付けてまとめ、
必要なときに呼び出せる仕組み。

・例えると「呼べば同じ作業をしてくれる道具」
・何度でも使える
・入力（引数）を受け取り、結果（戻り値）を返せる
'''


# ============================================================
# 【基本形】引数なし・戻り値なし
# ============================================================

def greet():            # 関数を定義するときは "def（define＝定義する の略）" を付ける。":" も忘れずに！
    name = "鈴木"
    print(name, "さん、こんにちは！")   # 処理は必ずインデント（字下げ）する！

greet()                 # 関数を呼び出すときは "関数名()" と書く

# 関数名のルールも変数名のルールと同じ！


# ============================================================
# 【引数】関数に値を渡す
# ============================================================

'''
「引数（ひきすう）」とは、関数に渡す入力データのこと。
・関数が処理するための材料
・呼び出すときに指定する
・関数の中で変数のように使える
'''

def greet_name(name):           # "name" が「引数」
    print(f"こんにちは、{name}さん")

greet_name("佐藤")              # 呼び出すとき、() 内の "佐藤" が引数として渡される


# 【f文字列について】
# 文字列の先頭に "f" をつけると、{ } の中に変数を直接書き込める！
# 例：
player = "勇者"
hp = 100
print(f"{player} の HP は {hp} です")   # → 勇者 の HP は 100 です


# ============================================================
# 【戻り値】関数から値を返す
# ============================================================

'''
「戻り値（もどりち）」とは、関数が処理した結果として返す値のこと。
・関数の出口から出てくる成果物
・return 文で指定する
・戻り値は変数に入れて、後から使える
'''

def add(a, b):          # 引数は複数設定できる（カンマ区切り）
    return a + b        # a + b を「戻り値」として返す

result = add(3, 5)      # 戻り値 8 が 変数 result に代入される
print(result)           # 8 を表示

# 戻り値を変数に入れておくと、後からいろいろ使える！
print(result * 2)       # 16（再利用の例）
print(result + 100)     # 108（こんな使い方もできる）


# 【print() だけの関数との違い】
# 以下のように print() で表示するだけの関数でも、見た目の結果は同じ…
def add_print(a, b):
    c = a + b
    print(c)

add_print(3, 5)         # 8 を表示

# しかし！print() だけだと、関数の外では値を使えない。
# 戻り値（return）にすることで、関数から値を受け取って保存・再利用できる。