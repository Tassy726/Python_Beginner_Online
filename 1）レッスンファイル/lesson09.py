# レッスン９「関数」

'''
「関数」とは、一連の処理に名前を付けてまとめ、
必要なときに呼び出せる仕組み。

・例えると「呼べば同じ作業をしてくれる道具」
・何度でも使える
・入力（引数）を受け取り、結果（戻り値）を返せる
'''


# ============================================================
# 【基本形】引数なし・戻り値なし
# ============================================================

def greet():            # 関数を定義するときは "def（define＝定義する の略）" を付ける。":" も忘れずに！
    name = "鈴木"
    print(name, "さん、こんにちは！")   # 処理は必ずインデント（字下げ）する！

greet()                 # 関数を呼び出すときは "関数名()" と書く

# 関数名のルールも変数名のルールと同じ！


# ============================================================
# 【引数】関数に値を渡す
# ============================================================

'''
「引数（ひきすう）」とは、関数に渡す入力データのこと。
・関数が処理するための材料
・呼び出すときに指定する
・関数の中で変数のように使える
'''

def greet(name):        # "name" が「引数」。さっきの greet() と見比べてみよう！
    print(name, "さん、こんにちは！")

greet("佐藤")           # 呼び出すとき、() 内の "佐藤" が引数として渡される
greet("田中")           # 引数を変えるだけで、違う名前に対応できる！


# ============================================================
# 【戻り値】関数から値を返す
# ============================================================

'''
「戻り値（もどりち）」とは、関数が処理した結果として返す値のこと。
・関数の出口から出てくる成果物
・return 文で指定する
・戻り値は変数に入れて、後から使える
'''

def add(a, b):          # 引数は複数設定できる（カンマ区切り）
    return a + b        # a + b を「戻り値」として返す

result = add(3, 5)      # 戻り値 8 が 変数 result に代入される
print(result)           # 8 を表示

# 戻り値を変数に入れておくと、後からいろいろ使える！
print(result * 2)       # 16（再利用の例）
print(result + 100)     # 108（こんな使い方もできる）


# 【print() だけの関数との違い】
# 以下のように print() で表示するだけの関数は、処理が関数の中で完結してしまう。
def add_print(a, b):
    print(a + b)

# result2 に代入しようとすると…
result2 = add_print(3, 5)   # 関数が実行されて 8 は表示される
                             # しかし return がないので result2 には None（空っぽ）が入る
print(result2)              # → None

# None が入っているので、後から使おうとするとエラーになる
# print(result2 + 10)       # → エラー！ None に数字は足せない

# 💡 関数の処理結果を外で使いたいときは、return を使おう！
# return を使えば、結果が関数の外に出てくるので、変数に入れて自由に使える。
result = add(3, 5)          # result に 8 が代入される
print(result + 10)          # → 18（後から使える！）


# ============================================================
# 【f文字列】文字列の中に変数を埋め込む
# ============================================================

'''
文字列と変数をつなげる方法は3つある。それぞれに欠点がある…

  方法１）カンマ「,」  → 文字列でも数値でもつなげられる。でも間に自動でスペースが入ってしまう。
  方法２）プラス「+」  → スペースは入らない。でも数値は str() に変換しないといけない。
  方法３）f文字列     → カンマとプラスの欠点を両方カバーできる！
'''

player = "勇者"
hp = 100

# 方法１）カンマ
print(player, "のHPは", hp, "です")            # → 勇者 のHPは 100 です（スペースが自動で入る）

# 方法２）プラス（数値は str() に変換が必要）
print(player + "のHPは" + str(hp) + "です")    # → 勇者のHPは100です（スペースなし）

# 方法３）f文字列（どちらの欠点もない！）
print(f"{player}のHPは{hp}です")               # → 勇者のHPは100です（スペースなし＆変換不要）

# さらに { } の中で計算もできる！
attack = 50
print(f"2倍攻撃のダメージは{attack * 2}です")  # → 2倍攻撃のダメージは100です